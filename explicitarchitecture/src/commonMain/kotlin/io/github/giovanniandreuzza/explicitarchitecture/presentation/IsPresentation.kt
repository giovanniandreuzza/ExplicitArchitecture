 * Is Presentation.
 * The presentation layer is the place where the User interactions are handled.
 * It is the driving force of the application.
 *
 * @author Giovanni Andreuzza
 */
@Target(AnnotationTarget.CLASS)
@Retention(AnnotationRetention.RUNTIME)
public annotation class IsPresentation